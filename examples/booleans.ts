// true or false

let a = 3;
let b = 2;

console.log(a === b); // false
console.log(a !== b); // true
console.log(a > b); // true
console.log(a < b); // false
console.log(a >= 3);
console.log(b <= 2);

// logical operators
console.log(true && false);
console.log(true && true);
console.log(false || false);
console.log(false || true);

console.log(1 + 1 === 2 && 10 * 10 > 50);
console.log(true ? 1 : 2);
console.log(false ? 1 : 2);
